import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int T=sc.nextInt();
        while(T-->0){
            int X=sc.nextInt();
            int Y=sc.nextInt();
            if(X>Y){
                System.out.println("A");
            }else{
                System.out.println("B");
            }
        }
    }
    }
